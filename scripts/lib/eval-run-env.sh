#!/bin/sh

# ROOT_DIR is supplied by scripts/eval-run.sh.
PLAYGROUND_TMP_DIR=${PLAYGROUND_TMP_DIR:-"$ROOT_DIR/tmp"}
LOCAL_EVM_DEPLOYMENT_FILE=${LOCAL_EVM_DEPLOYMENT_FILE:-"$PLAYGROUND_TMP_DIR/local-escrow-deployment.json"}
AUTOMATON_INBOX_DEPLOYMENT_FILE=${AUTOMATON_INBOX_DEPLOYMENT_FILE:-"$PLAYGROUND_TMP_DIR/automaton-inbox-deployment.json"}
PLAYGROUND_FACTORY_CANISTER_ID_FILE=${PLAYGROUND_FACTORY_CANISTER_ID_FILE:-"$PLAYGROUND_TMP_DIR/factory-canister-id.txt"}
PLAYGROUND_SERVICE_DIR=${PLAYGROUND_SERVICE_DIR:-"$PLAYGROUND_TMP_DIR/playground-services"}
PLAYGROUND_STATUS_FILE=${PLAYGROUND_STATUS_FILE:-"$PLAYGROUND_TMP_DIR/playground-status.json"}

port_from_url() {
  authority=${1#*://}
  authority=${authority%%/*}
  candidate=${authority##*:}
  case "$candidate" in
    ''|*[!0-9]*) return 1 ;;
    *) printf '%s' "$candidate" ;;
  esac
}

LOCAL_EVM_PORT=${LOCAL_EVM_PORT:-$(port_from_url "${LOCAL_EVM_RPC_URL:-http://127.0.0.1:8545}")}
PLAYGROUND_INDEXER_PORT=${PLAYGROUND_INDEXER_PORT:-$(port_from_url "${PLAYGROUND_INDEXER_BASE_URL:-$LAUNCHPAD_INDEXER_BASE_URL}")}
PLAYGROUND_RPC_GATEWAY_PORT=${PLAYGROUND_RPC_GATEWAY_PORT:-$(port_from_url "${PLAYGROUND_RPC_GATEWAY_URL:-${PLAYGROUND_PUBLIC_RPC_URL:-http://127.0.0.1:3002}}")}

# Keep base URLs aligned when only custom ports are supplied by callers.
PLAYGROUND_INDEXER_BASE_URL=${PLAYGROUND_INDEXER_BASE_URL:-"http://127.0.0.1:$PLAYGROUND_INDEXER_PORT"}
PLAYGROUND_RPC_GATEWAY_URL=${PLAYGROUND_RPC_GATEWAY_URL:-"http://127.0.0.1:$PLAYGROUND_RPC_GATEWAY_PORT"}
PLAYGROUND_PUBLIC_RPC_URL=${PLAYGROUND_PUBLIC_RPC_URL:-$PLAYGROUND_RPC_GATEWAY_URL}

export PLAYGROUND_TMP_DIR LOCAL_EVM_DEPLOYMENT_FILE AUTOMATON_INBOX_DEPLOYMENT_FILE PLAYGROUND_FACTORY_CANISTER_ID_FILE
export PLAYGROUND_SERVICE_DIR PLAYGROUND_STATUS_FILE LOCAL_EVM_PORT PLAYGROUND_INDEXER_PORT PLAYGROUND_RPC_GATEWAY_PORT
